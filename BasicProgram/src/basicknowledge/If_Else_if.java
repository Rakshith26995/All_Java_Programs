public class If_Else_if {

	public static void main(String[] args) {
		
		int a1 = 99;
		int a2 = 24;
		int a3 = 99;
		int a4 = 150;
		
		if(a1<a2)
		{
			System.out.println("1");
		}
		else if(a3>=a4) {
			System.out.println("2");
		}
		else if(a3==a4)
		{
			System.out.println("3");
		}
		else if(a2>a4)
		{
			System.out.println("4");
		}
		else
		{
			System.out.println("5");
		}
		
		
	}

}
